#' @noRd
enhance_enzyme.glyenzy_starter_gt_enzyme <- function(x) {
  x$rules <- purrr::map(x$rules, enhance_enzyme_rule_starter)
  x
}

#' @export
#' @noRd
enhance_enzyme.glyenzy_gt_enzyme <- function(x) {
  x$rules <- purrr::map(x$rules, enhance_enzyme_rule_gt)
  x
}

#' @export
#' @noRd
enhance_enzyme.glyenzy_gh_enzyme <- function(x) {
  x$rules <- purrr::map(x$rules, enhance_enzyme_rule_gh)
  x
}

#' @export
#' @noRd
enhance_enzyme.glyenzy_enzyme <- function(x) {
  switch(
    x$type,
    GT = if (.is_starter_gt(x)) {
      enhance_enzyme.glyenzy_starter_gt_enzyme(x)
    } else {
      enhance_enzyme.glyenzy_gt_enzyme(x)
    },
    GH = enhance_enzyme.glyenzy_gh_enzyme(x),
    cli::cli_abort("Unsupported enzyme type: {x$type}")
  )
}

#' Create a new enzyme rule object
#'
#' An enzyme rule defines one reaction of an enzyme.
#' An enzyme can have multiple rules.
#'
#' @param acceptor A `glyrepr_structure` scalar, representing the motif recognized by the enzyme.
#' @param product A `glyrepr_structure` scalar, representing the product generated by the enzyme.
#' @param acceptor_alignment A character string, representing the alignment of the `acceptor`.
#' @param rejects A `glyrepr_structure` vector, representing the motifs that the enzyme rejects.
#'
#' @noRd
new_enzyme_rule <- function(acceptor, product, acceptor_alignment, rejects) {
  checkmate::assert_class(acceptor, "glyrepr_structure")
  checkmate::assert_class(product, "glyrepr_structure")
  checkmate::assert_choice(
    acceptor_alignment,
    c("substructure", "core", "terminal", "whole")
  )
  checkmate::assert_class(rejects, "glyrepr_structure")

  structure(
    list(
      acceptor = acceptor,
      product = product,
      acceptor_alignment = acceptor_alignment,
      rejects = rejects
    ),
    class = "glyenzy_enzyme_rule"
  )
}

#' Validate fields shared by all enzyme rules
#'
#' @param x A `glyenzy_enzyme_rule` object.
#'
#' @noRd
validate_enzyme_rule_common <- function(x) {
  checkmate::assert_class(x, "glyenzy_enzyme_rule")

  if (length(x$acceptor) > 1) {
    cli::cli_abort("The {.arg acceptor} must be a single structure.")
  }
  if (length(x$product) != 1) {
    cli::cli_abort("The {.arg product} must be a single structure.")
  }

  invisible(x)
}

#' Validate reject rules shared by non-starter enzyme rules
#'
#' @param x A `glyenzy_enzyme_rule` object.
#'
#' @noRd
validate_enzyme_rule_rejects <- function(x) {
  if (length(x$rejects) == 0L) {
    return(invisible(x))
  }

  if (x$acceptor_alignment == "whole") {
    cli::cli_abort(
      "Cannot set {.field rejects} when the acceptor alignment is {.val whole}."
    )
  }
  if (
    !all(glymotif::have_motifs(
      x$rejects,
      x$acceptor,
      alignments = "substructure"
    ))
  ) {
    cli::cli_abort(
      "The {.arg acceptor} must be the substructure of all {.arg rejects}."
    )
  }
  n_mono_acc <- igraph::vcount(glyrepr::get_structure_graphs(
    x$acceptor,
    return_list = FALSE
  ))
  n_mono_rej <- glyrepr::smap_int(x$rejects, ~ igraph::vcount(.x))
  if (any(n_mono_rej == n_mono_acc)) {
    cli::cli_abort(
      "{.field rejects} cannot be the same as {.field acceptor}."
    )
  }

  invisible(x)
}

#' Validate a regular GT enzyme rule
#'
#' @param x A `glyenzy_enzyme_rule` object.
#'
#' @noRd
validate_enzyme_rule_gt <- function(x) {
  validate_enzyme_rule_common(x)
  .check_product_acceptor(x$acceptor, x$product, "acceptor", "product")
  validate_enzyme_rule_rejects(x)
  invisible(x)
}

#' Validate a starter GT enzyme rule
#'
#' @param x A `glyenzy_enzyme_rule` object.
#'
#' @noRd
validate_enzyme_rule_starter <- function(x) {
  validate_enzyme_rule_common(x)
  if (length(x$rejects) > 0) {
    cli::cli_abort("{.field rejects} must be empty for starter GTs.")
  }
  invisible(x)
}

#' Validate a GH enzyme rule
#'
#' @param x A `glyenzy_enzyme_rule` object.
#'
#' @noRd
validate_enzyme_rule_gh <- function(x) {
  validate_enzyme_rule_common(x)
  .check_product_acceptor(x$product, x$acceptor, "product", "acceptor")
  validate_enzyme_rule_rejects(x)
  invisible(x)
}

#' Enhance a starter GT rule
#'
#' @param x A starter `glyenzy_enzyme_rule` object.
#'
#' @returns A starter `glyenzy_enzyme_rule` with derived rule fields.
#' @noRd
enhance_enzyme_rule_starter <- function(x) {
  product_graph <- glyrepr::get_structure_graphs(x$product, return_list = FALSE)

  x$acceptor_idx <- 0L
  x$product_idx <- 1L
  x$new_residue <- igraph::V(product_graph)[[1]]$mono
  x$new_linkage <- NULL
  x
}

#' Enhance a regular GT rule
#'
#' @param x A `glyenzy_enzyme_rule` object.
#'
#' @returns An enhanced `glyenzy_enzyme_rule`.
#' @noRd
enhance_enzyme_rule_gt <- function(x) {
  acceptor_graph <- glyrepr::get_structure_graphs(
    x$acceptor,
    return_list = FALSE
  )
  product_graph <- glyrepr::get_structure_graphs(x$product, return_list = FALSE)
  match_res <- glymotif::match_motif(
    x$product,
    x$acceptor,
    alignment = "core"
  )[[1]][[1]] # only one glycan and one match, so `[[1]][[1]]`

  # The node index of the new residue in the product
  new_residue_idx <- setdiff(1:igraph::vcount(product_graph), match_res)

  # Get the index of its parent node
  parent_idx <- igraph::neighbors(product_graph, new_residue_idx, mode = "in")
  # And map it to the acceptor graph
  acceptor_idx <- match(parent_idx, match_res)

  # Get the new residue and new linkage
  new_residue <- igraph::V(product_graph)[new_residue_idx]$mono
  new_linkage <- igraph::incident(
    product_graph,
    new_residue_idx,
    mode = "in"
  )$linkage

  # Add new fields
  x$acceptor_idx <- acceptor_idx
  x$product_idx <- new_residue_idx # For GT: index of newly added residue in product
  x$new_residue <- new_residue
  x$new_linkage <- new_linkage
  x
}

#' Enhance a GH rule
#'
#' @param x A `glyenzy_enzyme_rule` object.
#'
#' @returns An enhanced `glyenzy_enzyme_rule`.
#' @noRd
enhance_enzyme_rule_gh <- function(x) {
  acceptor_graph <- glyrepr::get_structure_graphs(
    x$acceptor,
    return_list = FALSE
  )
  product_graph <- glyrepr::get_structure_graphs(x$product, return_list = FALSE)
  match_res <- glymotif::match_motif(
    x$acceptor,
    x$product,
    alignment = "core"
  )[[1]][[1]] # only one glycan, so `[[1]]`

  # The node index of the removed residue in the acceptor
  removed_residue_idx <- setdiff(1:igraph::vcount(acceptor_graph), match_res)

  # Add new fields
  x$acceptor_idx <- removed_residue_idx
  x$product_idx <- NULL # For GH: no new residue is added
  x$new_residue <- NULL
  x$new_linkage <- NULL
  x
}

#' Check a product-acceptor pair
#'
#' This function checks if a product-acceptor pair is valid.
#' To be a valid pair, the following conditions must be met:
#' 1. `smaller` is a substructure of `larger`
#' 2. `larger` has exactly one more residue than `smaller`
#'
#' @param smaller A `glyrepr_structure` scalar representing the smaller structure.
#' @param larger A `glyrepr_structure` scalar representing the larger structure.
#' @param smaller_name A character string representing the name of the smaller structure.
#'   Used for error messages.
#' @param larger_name A character string representing the name of the larger structure.
#'   Used for error messages.
#'
#' @noRd
.check_product_acceptor <- function(
  smaller,
  larger,
  smaller_name,
  larger_name
) {
  smaller_graph <- glyrepr::get_structure_graphs(smaller, return_list = FALSE)
  larger_graph <- glyrepr::get_structure_graphs(larger, return_list = FALSE)
  match_res <- glymotif::match_motif(larger, smaller, alignment = "core")[[1]] # only one glycan, so `[[1]]`
  # `smaller` is a substructure of `larger`
  if (length(match_res) != 1) {
    cli::cli_abort(
      "{.arg {smaller_name}} must be a substructure of {.arg {larger_name}}."
    )
  }
  match_res <- match_res[[1]] # only one match, so `[[1]]`
  # `larger` has only one more residue than `smaller`
  if (igraph::vcount(larger_graph) - igraph::vcount(smaller_graph) != 1) {
    cli::cli_abort(
      "{.arg {larger_name}} must have exactly one more residue than {.arg {smaller_name}}."
    )
  }
}

#' Check a product-acceptor pair for starter GTs
#'
#' This function checks if a product-acceptor pair is valid for starter GTs.
#' @param acceptor A `glyrepr_structure` scalar representing the acceptor structure.
#' @param product A `glyrepr_structure` scalar representing the product structure.
#' @noRd
.check_starter_product_acceptor <- function(acceptor, product) {
  if (length(acceptor) != 0) {
    cli::cli_abort("The acceptor must be an empty {.cls glyrepr_structure} vector for starter GTs.")
  }
  n_mono <- glyrepr::count_mono(product)
  if (length(n_mono) != 1L || n_mono != 1L) {
    cli::cli_abort(
      "{.arg product} must be a monosaccharide for a starter GT."
    )
  }
  invisible(NULL)
}

#' Check whether an enzyme is a starter GT
#'
#' @param x A `glyenzy_enzyme` object.
#'
#' @returns `TRUE` if `x` is a GT enzyme with at least one empty acceptor rule.
#' @noRd
.is_starter_gt <- function(x) {
  inherits(x, "glyenzy_starter_gt_enzyme")
}

#' Print method for glyenzy_enzyme objects
#'
#' @param x A `glyenzy_enzyme` object.
#' @param ... Additional arguments passed to print methods.
#' @export
print.glyenzy_enzyme <- function(x, ...) {
  cli::cli_h1("Enzyme: {.field {x$name}}")

  # Basic information
  type_str <- .enzyme_type_label(x)
  cli::cli_alert_info("Type: {.val {x$type}} ({.emph {type_str}})")
  cli::cli_alert_info("Species: {.val {x$species}}")

  # Rules section
  cli::cli_h2("Rules ({.val {length(x$rules)}})")
  if (length(x$rules) > 0) {
    purrr::iwalk(x$rules, function(rule, i) {
      cli::cli_alert(
        "Rule {.val {i}}: {.field {rule$acceptor_alignment}} alignment"
      )
      cli::cli_text("  Acceptor: {.val {as.character(rule$acceptor)}}")
      cli::cli_text("  Product:  {.val {as.character(rule$product)}}")

      # Show rejects for this rule
      if (length(rule$rejects) > 0) {
        cli::cli_text("  Rejects:")
        purrr::iwalk(rule$rejects, function(reject, j) {
          cli::cli_text("    {.val {as.character(reject)}}")
        })
      }
    })
  } else {
    cli::cli_text("  {.emph No rules defined}")
  }

  invisible(x)
}

