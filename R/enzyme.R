#' Enzymes
#'
#' @description
#' Two types of enzymes are involved in glycosylation: glycosyltransferases (GTs) and glycoside hydrolases (GHs).
#' - GTs catalyze the transfer of a sugar residue from a donor to an acceptor, thus building up glycan structures.
#' - GHs catalyze the removal of a sugar residue from a substrate, thus breaking down glycan structures.
#'
#' One special subtype of GTs are starter GTs (or initiating GTs),
#' which catalyze the addition of the first sugar residue onto a non-glycan substrate, thus initiating glycosylation.
#'
#' Use `enzyme()` with a gene symbol to load a predefined enzyme.
#' For example, use `enzyme("ST3GAL3")` to load the enzyme ST3GAL3.
#'
#' Throughout the package, you can use `enzyme()`s for any `enzyme` argument,
#' or just use the gene symbol directly.
#' For example, `involve("Neu5Ac(a2-3)Gal(b1-3)GalNAc(a1-", "ST3GAL3")` and
#' `involve("Neu5Ac(a2-3)Gal(b1-3)GalNAc(a1-", enzyme("ST3GAL3"))` are equivalent.
#'
#' @details
#'
#' # Explanation about `glyenzy_enzyme`
#'
#' An `enzyme()` is a list with the following elements:
#'
#' 1. `name`: the name of the enzyme, usually the gene symbol.
#' 2. `rules`: a list of `glyenzy_enzyme_rule` objects.
#'    Each rule is a list with the following fields:
#'    - `acceptor`: the motif that the enzyme recognizes
#'    - `acceptor_alignment`: the alignment of the `acceptor`
#'    - `rejects`: the motifs that the enzyme rejects to act on
#'    - `product`: the product generated by the enzyme
#'    - `acceptor_idx`: the node index of the acceptor where the enzyme acts on.
#'      For GTs, this is the node new residue is attached to.
#'      For GHs, this is the node that is removed.
#'      For starter GTs, `acceptor_idx` is 0.
#'    - `product_idx`: the node index of the product residue in the product structure.
#'      For GTs, this is the index of the newly added residue in the product.
#'      For GHs, this is `NULL` (no new residue is added).
#'    - `new_residue`: the new residue added by the enzyme. For GHs, this is `NULL`.
#'    - `new_linkage`: the linkage of the new residue. For GHs, this is `NULL`.
#' 3. `type`: the broad type of the enzyme, "GT" for glycosyltransferase or
#'    "GH" for glycoside hydrolase. Starter GTs are encoded as `type = "GT"`.
#' 4. `species`: the species of the enzyme, e.g. "human" or "mouse".
#'
#' You can see all these information by printing the enzyme object.
#'
#' @param symbol The gene symbol of the enzyme.
#'
#' @return A `glyenzy_enzyme` object.
#'
#' @examples
#' library(glyrepr)
#'
#' enzyme("ST3GAL3")
#'
#' @export
enzyme <- function(symbol) {
  checkmate::assert_string(symbol)
  if (symbol %in% names(glyenzy_enzymes)) {
    glyenzy_enzymes[[symbol]]
  } else {
    cli::cli_abort("Unknown enzyme: {.val {symbol}}.")
  }
}

#' Get all enzymes
#'
#' Return a named list of all built-in enzymes,
#' or a character vector of gene symbols if `return_str` is `TRUE`.
#'
#' @param return_str If `FALSE` (default), returns the enzyme list.
#'   Otherwise returns a character vector of gene symbols.
#' @param include_starter_gt If `TRUE` (default), includes starter GTs in the result.
#'   Starter GTs are enzymes that initiate glycosylation by introducing the first sugar residue onto a non-glycan substrate.
#'   For example, DPAGT1 is the starter GT for N-glycosylation.
#'
#' @returns A list of [enzyme()]s or a character vector.
#' @examples
#' db_enzymes(return_str = TRUE)
#'
#' @export
db_enzymes <- function(return_str = FALSE, include_starter_gt = TRUE) {
  if (include_starter_gt) {
    enzymes <- glyenzy_enzymes
  } else {
    enzymes <- purrr::discard(glyenzy_enzymes, .is_starter_gt)
  }
  if (return_str) {
    return(names(enzymes))
  } else {
    return(enzymes)
  }
}

#' Create a new enzyme object
#'
#' @param name The name of the enzyme.
#' @param rules A list of `glyenzy_enzyme_rule` objects.
#' @param type The type of the enzyme, "GT" for glycosyltransferase or "GH" for glycoside hydrolase.
#' @param species The species of the enzyme, e.g. "human" or "mouse".
#'
#' @return A `glyenzy_enzyme` object.
#' @noRd
new_enzyme <- function(name, rules, type, species) {
  checkmate::assert_string(name)
  checkmate::assert_list(rules, types = "glyenzy_enzyme_rule")
  checkmate::assert_choice(type, c("GT", "GH"))
  checkmate::assert_string(species)

  structure(
    list(name = name, rules = rules, type = type, species = species),
    class = .enzyme_classes(name, type)
  )
}

#' Determine S3 classes for an enzyme
#'
#' Classes:
#' - "glyenzy_enzyme": the base class for all enzymes.
#'   - "glyenzy_gt_enzyme": for glycosyltransferases (GTs).
#'    - "glyenzy_starter_gt_enzyme": for starter GTs.
#'    - "glyenzy_npre_gt_enzyme": for GTs involved in N-glycan precursor synthesis.
#'  - "glyenzy_gh_enzyme": for glycoside hydrolases (GHs).
#'
#' @param name The name of the enzyme.
#' @param type The type of the enzyme, "GT" for glycosyltransferase or "GH" for glycoside hydrolase.
#'
#' @returns A character vector of S3 class names.
#' @noRd
.enzyme_classes <- function(name, type) {
  starter_gts <- c(
    "DPAGT1", "FUT10", "FUT11", "POFUT1", "POFUT2",
    "POGLUT1", "POGLUT2", "POGLUT3", "POMT1", "POMT2",
    "TMTC1", "TMTC2", "TMTC3", "TMTC4",
    paste0("GALNT", 1:19)
  )
  cls <- switch(
    type,
    GT = c("glyenzy_gt_enzyme", "glyenzy_enzyme"),
    GH = c("glyenzy_gh_enzyme", "glyenzy_enzyme")
  )
  if (name %in% starter_gts) {
    cls <- c("glyenzy_starter_gt_enzyme", cls)
  }
  if (stringr::str_starts(name, "ALG")) {
    cls <- c("glyenzy_npre_gt_enzyme", cls)
  }
  cls
}

#' Validate a `glyenzy_enzyme` object
#'
#' @param x A `glyenzy_enzyme` object.
#' @noRd
validate_enzyme <- function(x) {
  checkmate::assert_class(x, "glyenzy_enzyme")
  UseMethod("validate_enzyme", x)
}

#' @export
#' @noRd
validate_enzyme.glyenzy_starter_gt_enzyme <- function(x) {
  purrr::walk(x$rules, validate_enzyme_rule_starter)
  invisible(x)
}

#' @export
#' @noRd
validate_enzyme.glyenzy_gt_enzyme <- function(x) {
  purrr::walk(x$rules, validate_enzyme_rule_gt)
  invisible(x)
}

#' @export
#' @noRd
validate_enzyme.glyenzy_gh_enzyme <- function(x) {
  purrr::walk(x$rules, validate_enzyme_rule_gh)
  invisible(x)
}

#' @export
#' @noRd
validate_enzyme.glyenzy_enzyme <- function(x) {
  switch(
    x$type,
    GT = if (.is_starter_gt(x)) {
      validate_enzyme.glyenzy_starter_gt_enzyme(x)
    } else {
      validate_enzyme.glyenzy_gt_enzyme(x)
    },
    GH = validate_enzyme.glyenzy_gh_enzyme(x),
    cli::cli_abort("Unsupported enzyme type: {x$type}")
  )
  invisible(x)
}

#' Enhance a `glyenzy_enzyme` object
#'
#' This function dispatches on the enzyme class, then enhances all rules with
#' enzyme-type-specific helper functions.
#'
#' @param x A `glyenzy_enzyme` object.
#' @noRd
enhance_enzyme <- function(x) {
  checkmate::assert_class(x, "glyenzy_enzyme")
  UseMethod("enhance_enzyme", x)
}

